    function interpolate(loc1, loc2, value1, value2) {
        if (setting.interpolate === 1) {
            this.v = [(loc1[0] + loc2[0]) / 2, (loc1[1] + loc2[1]) / 2, (loc1[2] + loc2[2]) / 2];
        } else {
            var P = new Array(3);
            if (Math.abs(1 - value1) < 0.0005) {
                P = loc1;
            } else if (Math.abs(1 - value2) < 0.0005) {
                P = loc2;
            } else if (Math.abs(value1 - value2) < 0.0005) {
                P = loc1;
            } else {
                var value = (1 - value1) / (value2 - value1);
                P[0] = loc1[0] + value * (loc2[0] - loc1[0]);
                P[1] = loc1[1] + value * (loc2[1] - loc1[1]);
                P[2] = loc1[2] + value * (loc2[2] - loc1[2]);
            }
            this.v = P;
        }
    }

    function calcfps() {
        counter++;
        time = Date.now();
        if (time - timeOld >= 1000) {
            fps = counter;
            timeOld = time;
            counter = 0;
        }
    }
