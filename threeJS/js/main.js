    function calcFuncValues() {
        for (var i = 0; i < setting.steps + 1; i++) {
            for (var j = 0; j < setting.steps + 1; j++) {
                for (var k = 0; k < setting.steps + 1; k++) {
                    valuesMass[i][j][k] = func(pointsMass[i][j][k]);
                }
            }
        }
    }

    function func(pointLoc) {
        var sum = 0;
        for (var i = 0; i < points.length; i++) {
            sum += Math.pow(setting.size, 2) / ((Math.pow(pointLoc[0] - points[i].x, 2) + Math.pow(pointLoc[1] - points[i].y, 2) + (Math.pow(pointLoc[2] - points[i].z, 2))));
        }
        return sum;
    }

