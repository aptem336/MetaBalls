    function step() {
        for (var i = 0; i < points.length; i++) {
            if (Math.abs(points[i].x) + setting.size >= 100 - setting.size) {
                points[i].speedX = -points[i].speedX;
            }
            if (Math.abs(points[i].y) + setting.size >= 100 - setting.size) {
                points[i].speedY = -points[i].speedY;
            }
            if (Math.abs(points[i].z) + setting.size >= 100 - setting.size) {
                points[i].speedZ = -points[i].speedZ;
            }
            points[i].x += points[i].speedX * setting.speed / 5;
            points[i].y += points[i].speedY * setting.speed / 5;
            points[i].z += points[i].speedZ * setting.speed / 5;
        }
    }

